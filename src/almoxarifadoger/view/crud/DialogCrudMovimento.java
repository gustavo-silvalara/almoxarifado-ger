    /**
     * @param args the command line arguments
     */
    private void carregaItems() {
        DAOItem daoI = new DAOItem();
        List<Item> items = daoI.read("");
        DefaultComboBoxModel m = (DefaultComboBoxModel) jcbItem.getModel();
        m.removeAllElements();
        if (!items.isEmpty()) {
            for (Item i : items) {
                m.addElement(i);
            }
        }
    }

    private void carregaResponsaveis() {
        DAOResponsavel daoR = new DAOResponsavel();
        List<Responsavel> responsaveis = daoR.read("");
        DefaultComboBoxModel m = (DefaultComboBoxModel) jcbResp.getModel();
        m.removeAllElements();
        if (!responsaveis.isEmpty()) {
            for (Responsavel i : responsaveis) {
                m.addElement(i);
            }
        }
    }

    private void carregaAlmoxarifado() {
        DAOAlmoxarifado daoA = new DAOAlmoxarifado();
        List<Almoxarifado> almoxarifado = daoA.read("");
        DefaultComboBoxModel m = (DefaultComboBoxModel) jcbAlmox.getModel();
        m.removeAllElements();
        if (!almoxarifado.isEmpty()) {
            for (Almoxarifado i : almoxarifado) {
                m.addElement(i);
            }
        }
    }

    private void getDadosDaTela() {
        registroEmEdicao = new MovimentoItemAlmox();
        registroEmEdicao.setData(System.currentTimeMillis());
        registroEmEdicao.setTipo(jcbTipo.getSelectedIndex());
        ItemAlmoxarifado iA = new ItemAlmoxarifado();
        if (jcbAlmox.getItemCount() > 0) {
            iA.setAlmoxarifado((Almoxarifado) jcbAlmox.getSelectedItem());
        }
        if (jcbItem.getItemCount() > 0) {
            iA.setItem((Item) jcbItem.getSelectedItem());
        }
        registroEmEdicao.setItemAlmox(iA);
        if (jcbResp.getItemCount() > 0) {
            registroEmEdicao.setResponsavel((Responsavel) jcbResp.getSelectedItem());
        }
        
    }

